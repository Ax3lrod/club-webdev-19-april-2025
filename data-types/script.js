// String
let nama = "Satya";
console.log(nama); // "Satya"

// Number
let umur = 20;
console.log(umur); // 20

// Boolean
let isAdmin = true;
console.log(isAdmin); // true

// Undefined
let alamat;
console.log(alamat); // undefined

// Null
let data = null;
console.log(data); // null

// BigInt
let big = 123456789012345678901234567890n;
console.log(big); // 123456789012345678901234567890n

// Symbol
let id = Symbol("unique");
console.log(id); // Symbol(unique)

